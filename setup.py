import os
import os.path
from setuptools import find_packages, setup
import codecs
# this comes form here https://packaging.python.org/guides/single-sourcing-package-version/
# here we get the version from pbr
def read(rel_path):
    here = os.path.abspath(os.path.dirname(__file__))
    with codecs.open(os.path.join(here, rel_path), 'r') as fp:
        return fp.read()

def get_version(rel_path):
    for line in read(rel_path).splitlines():
        if line.startswith('__version__'):
            delim = '"' if '"' in line else "'"
            return line.split(delim)[1]
    else:
        raise RuntimeError("Unable to find version string.")

# here we collect all package data files and expose them
def package_files(directory):
    paths = []
    for (path, directories, filenames) in os.walk(directory):
        for filename in filenames:
            paths.append(os.path.join('..', path, filename))
    return paths

setup(
    packages=['martini_forcefields'],
    package_data={'': package_files('martini_forcefields'), },
    pbr=True,
    version=get_version("martini_forcefields/__init__.py"),
    )
